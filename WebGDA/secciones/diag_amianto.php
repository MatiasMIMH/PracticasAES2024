    <div class="introduccion_texto">
        <p>El amianto constituye un serio problema de salud pública.</p>
        <p>La presencia de este material en la construcción e industria se debe a sus buenas condiciones como aislante térmico, eléctrico y acústico. Así como, por su resistencia mecánica, química y al fuego.</p>
        <p>Con el transcurso del tiempo, ha quedado demostrado su efecto nocivo sobre la salud.</p> 
        <p>El amianto está clasificado como carcinógeno categoría 1 A  según el Anexo VI del Reglamento (CE)  nº 1272/2008 (adjuntamos link con el documento)  y la International Agency for Research on Cancer de la Organización Mundial de la Salud. (adjuntamos link con el enlace https://www.iarc.who.int/)</p>
