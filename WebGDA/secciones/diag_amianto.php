    <div class="articulo_cuerpo_2">

        <div class="titulo_cuerpo">
            <h2>¿Qué dice la legislación sobre la identificación del amianto?</h2>
        </div>

        <div class="texto_cuerpo">
            <p>La Legislación Española sobre el amianto esta desarrollada entre otros por el RD 396/2006 de 31 de marzo.</p>
            <p>Establece en su artículo 10 “Antes del comienzo de obras de demolición o mantenimiento, los empresarios deberán adoptar todas las medidas adecuadas para identificar los materiales que puedan contener amianto. Si existe la menor duda sobre la presencia de amianto en un material o una construcción, deberán observarse las disposiciones de este real decreto que resulten de su aplicación.</p>
            <p>El incumplimiento de esta prescripción, de acuerdo con el art. 13.6 de la Ley de Infracciones y Sanciones de Orden Social (LISOS), RD 5/200 de 4 de agosto; es una FALTA MUY GRAVE, penada con multas entre 30.000/600.000 € y las correspondientes acciones penales.</p>
            <p>La Ley 7/2022, de 8 de abril, de residuos y suelos contaminados para una economía circular establece que, antes del 10 de abril de 2023, los ayuntamientos deben elaborar un censo de instalaciones y emplazamientos con amianto, incluyendo un calendario para su retirada.</p>
        </div>
