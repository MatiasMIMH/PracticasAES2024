            <h2>¿Por qué hay que realizar una inspección de amianto?</h2>
        </div>

        <div class="texto_cuerpo">
            <p>Detectar y analizar la presencia de amianto, nos permite evaluar el nivel de riesgo asociado a su estado de conservación y establecer un correcto proceso de eliminación.</p>
            <p>La evaluación y prevención de los riesgos que para trabajadores y terceras personas puedan estar relacionados con la exposición a fibras de amianto, es una necesidad tanto para empresas, como comunidades y particulares.</p>
        </div>
